from typing import Any

from app.core.llm import complete_text, dumps_for_prompt, render_prompt


async def generate_post(
    audience_profile: dict[str, Any],
    idea: dict[str, Any],
    cta: str = "Выбери тариф и получай такие посты регулярно.",
) -> str:
    prompt = render_prompt(
        "generate_post.md",
        AUDIENCE_PROFILE=dumps_for_prompt(audience_profile),
        IDEA=dumps_for_prompt(idea),
        CTA=cta,
    )
    response = await complete_text(prompt, temperature=0.75)
    if response is None:
        return mock_post(audience_profile, idea, cta)
    return response.strip()


def mock_post(audience_profile: dict[str, Any], idea: dict[str, Any], cta: str) -> str:
    niche = audience_profile.get("niche", "твоей нише")
    pain = (audience_profile.get("pains") or ["тексты не попадают в аудиторию"])[0]
    desire = (audience_profile.get("desires") or ["получать заявки из контента"])[0]
    title = idea.get("title", "Почему посты не приводят заявки")

    return (
        f"{title}\n\n"
        "Иногда проблема не в том, что вы редко пишете.\n\n"
        "Проблема в том, что пост начинается не с читателя.\n\n"
        f"В нише «{niche}» это особенно заметно: автор старается рассказать, какой у него опыт, "
        "какой продукт сильный и почему ему можно доверять. Всё это важно, но читатель в этот момент "
        "держит в голове другой вопрос: «А это вообще про меня?»\n\n"
        f"Если внутри у человека есть боль «{pain}», он цепляется не за красивую формулировку. "
        "Он цепляется за точное попадание. За фразу, после которой хочется сказать: да, у меня именно так.\n\n"
        "Например, можно написать:\n\n"
        "«Мы делаем экспертный контент для Telegram».\n\n"
        "А можно иначе:\n\n"
        "«Вы садитесь писать пост, открываете заметки, вспоминаете десяток умных мыслей, "
        "но в итоге публикуете общий текст, после которого нет ни заявок, ни вопросов в личке».\n\n"
        "Вторая версия работает сильнее не потому, что она длиннее. Она показывает ситуацию, "
        "в которой человек узнаёт себя.\n\n"
        f"Дальше важно дать не просто эмоцию, а инсайт. Например: чтобы прийти к результату "
        f"«{desire}», посту нужен не абстрактный смысл, а связка из боли, причины, примера и следующего шага.\n\n"
        "Боль привлекает внимание.\n\n"
        "Причина объясняет, почему проблема повторяется.\n\n"
        "Пример делает мысль конкретной.\n\n"
        "CTA показывает, что делать дальше.\n\n"
        "Посмотрите, как это можно применить в обычном посте.\n\n"
        "Сначала вы называете ситуацию: человек уже пробовал писать регулярно, но каждый раз упирается "
        "в один и тот же вопрос — о чем писать так, чтобы это было не ради галочки.\n\n"
        "Потом показываете цену этой проблемы: канал вроде живой, посты выходят, экспертность есть, "
        "но читатель не делает следующий шаг. Он не пишет в личку, не задает вопрос, не просит условия.\n\n"
        "После этого даете простой разворот: дело не в количестве постов, а в точности попадания. "
        "Один пост, который попал в боль, часто сильнее пяти общих публикаций про пользу продукта.\n\n"
        "И только затем можно мягко показать решение: перед тем как писать, разложить аудиторию на боли, "
        "желания, возражения и привычный язык. Тогда текст не приходится выдавливать. У него появляется "
        "понятная опора.\n\n"
        "Так текст перестаёт быть просто публикацией ради присутствия в канале. Он становится маленьким "
        "продающим активом: не давит, не кричит, но помогает читателю увидеть проблему и захотеть решение.\n\n"
        "Поэтому перед генерацией поста я сначала смотрю на аудиторию: кто читает, чего боится, "
        "чего хочет, какие формулировки звучат живо, а какие лучше не использовать.\n\n"
        "Без этого легко получить аккуратный, но пустой текст. С этим появляется шанс написать пост, "
        "который звучит как нормальный контент в Telegram и при этом ведёт к продаже.\n\n"
        f"{cta}"
    )
